# update this function to return the number of times `character` appears in `word`!
def count_occurrences(word, character):
    return word.count(character)

# Example usage
print(count_occurrences("banana", "a"))  # => 3
print(count_occurrences("sooper dooper", "o"))  # => 4
print(count_occurrences("alakazam", "a"))  # => 4
